class AssignmentoperatorDemo {
	public static void main(String[] args) {
		int a=10;
		a+=20;
		System.out.println("a: "+a);
		
		int b=10;
		b-=20;
		System.out.println("b: "+b);

		int c=10;
		c*=20;
		System.out.println("c: "+c);
		
		int d=10;
		d/=20;
		System.out.println("d: "+d);

		int e=10;
		e%=20;
		System.out.println("e: "+e);

}
}