class ArithmeticOperatorsDemo {
	public static void main(String[] args) {
		short s1=10;
		short s2=20;
		int s3=s1+s2;
		System.out.println("s1+s2="+s3);
		
		int i1=10;
		int i2=20;
		int i3=i1-i2;
		System.out.println("i1-i2="+i3);

		int j1=10;
		int j2=20;
		int j3=j1*j2;
		System.out.println("j1*j2="+j3);

		int k1=10;
		short k2=20;
		int k3=k1/k2;
		System.out.println("k1/k2="+k3);

		short l1=10;
		short l2=20;
		int l3=l1%l2;
		System.out.println("l1%l2="+l3);
}
}
		
